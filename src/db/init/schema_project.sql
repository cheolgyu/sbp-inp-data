DROP TABLE IF  EXISTS project.tb_monthly_peek;
CREATE TABLE IF NOT EXISTS project.tb_monthly_peek (
    code_id                  integer NOT NULL REFERENCES "meta"."code"(id)
   ,"is_peek" boolean
   ,"peek" numeric(2, 0) 
   ,"peek_range" numeric(2, 0)[]
   ,"peek_percent" numeric(10, 2) 
   ,"list" json
);

