    CREATE or replace FUNCTION public.get_percent(part numeric, total numeric)
    RETURNS  numeric AS $$
    BEGIN 
    RETURN  (select NULLIF(part,0)/NULLIF(total,0) *100 );
    END;  $$
    LANGUAGE plpgsql;
