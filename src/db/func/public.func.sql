    CREATE or replace FUNCTION public.get_o2c(op numeric, cp numeric)
    RETURNS  numeric AS $$
    BEGIN 
    RETURN  (select round((cp-op)/NULLIF(op,0) *100,2) );
    END;  $$
    LANGUAGE plpgsql;
